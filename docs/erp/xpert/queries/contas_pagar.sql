-- Contas a pagar (títulos em aberto) — base: Contas a Pagar Postos.txt
-- Limitação: não inclui títulos liquidados (DTAPGTO / REFERENCIAPGTO).
SELECT
    CP.ID_FILIAL,
    ISNULL(CP.ID_ENTIDADE, 0) AS ID_ENTIDADE,
    ENT.NOMEENTIDADE,
    CP.HISTORICO,
    CP.NRODOC,
    CP.DTACONTA,
    CP.DTAVCTO,
    CASE WHEN CP.DTAVCTO < GETDATE() THEN 'VENCIDO' ELSE 'A VENCER' END AS SITUACAO,
    CP.VALOR,
    SUM(ISNULL(CPB.VALORBAIXA, 0)) AS VALORBAIXADO,
    CP.VALOR - SUM(ISNULL(CPB.VALORBAIXA, 0)) AS VALORFALTANTE,
    CASE
        WHEN (CP.VALOR - SUM(ISNULL(CPB.VALORBAIXA, 0))) = 0 THEN 'LIQUIDADO'
        ELSE 'PENDENTE'
    END AS STATUS
FROM CONTASPAGAR CP
LEFT JOIN CONTASPAGARBAIXA CPB
    ON CP.ID_FILIAL = CPB.ID_FILIAL
    AND CP.ID_CONTASPAGAR = CPB.ID_CONTASPAGAR
LEFT JOIN ENTIDADES ENT
    ON CP.ID_FILIAL = ENT.ID_FILIAL
    AND CP.ID_ENTIDADE = ENT.ID_ENTIDADE
WHERE CP.DTAPGTO IS NULL
    AND CP.REFERENCIAPGTO IS NULL
    AND CP.DTACONTA <= GETDATE()
GROUP BY
    CP.ID_FILIAL,
    CP.ID_ENTIDADE,
    ENT.NOMEENTIDADE,
    CP.HISTORICO,
    CP.NRODOC,
    CP.ID_CONTASPAGAR,
    CP.DTACONTA,
    CP.DTAVCTO,
    CP.VALOR
ORDER BY CP.ID_FILIAL, CP.ID_ENTIDADE;
