SELECT
    CAST(NULL AS VARCHAR(50)) AS source_branch_id,
    CAST(NULL AS VARCHAR(50)) AS source_product_id,
    CAST(NULL AS VARCHAR(50)) AS source_payment_method_id,
    CAST(NULL AS DECIMAL(18, 8)) AS source_price_per_liter,
    CAST(NULL AS BIT) AS source_active,
    CAST(NULL AS DATETIME2) AS source_effective_from,
    CAST(NULL AS DATETIME2) AS source_effective_until,
    CAST(NULL AS DATETIME2) AS source_updated_at
WHERE 1 = 0;
